//Exercise 05: Trung bình tuổi của các user

function averageAge(users: User): number {
  if (users.length === 0) return 0;

  const totalAge = users.reduce((acc, user) => acc + user.age, 0);
  return totalAge / users.length;
}
//Exercise 0601: Tên đầy đủ của các user sử dụng reduce

function getFullNames(users: User[]): string[] {
  return users.reduce((acc, user) => {
    const fullName = `${user.first_name} ${user.last_name}`;
    acc.push(fullName);
    return acc;
  }, []);
}
//Exercise 0602: User nam dưới 40 tuổi sử dụng reduce

function getYoungMaleUsers(users: User[]): User[] {
  return users.reduce((acc, user) => {
    if (user.gender === "Male" && user.age < 40) {
      acc.push(user);
    }
    return acc;
  }, []);
}
//Exercise 0603: Chuyển đổi key thành camelCase sử dụng reduce

function convertToCamelCase(users: User[]): User[] {
  return users.reduce((acc, user) => {
    const camelCaseUser: User = {
      id: user.id,
      firstName: user.first_name,
      lastName: user.last_name,
      email: user.email,
      gender: user.gender,
      age: user.age,
      salary: user.salary,
    };
    acc.push(camelCaseUser);
    return acc;
  }, []);
}
//Exercise 07: Sắp xếp first_name theo thứ tự tăng dần sử dụng sort

function sortUsersByFirstName(users: User[]): User[] {
  return users.slice().sort((a, b) => a.first_name.localeCompare(b.first_name));
}
